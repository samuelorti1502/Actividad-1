    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        ClsUsuario usuario = new ClsUsuario();

        String pass = null;
        String pass2 = null;
        //ackgroundImage1 back = new BackgroundImage1();

        validarCampos();

        if (!isValidador()) {
            if (String.valueOf(txtContra1.getPassword()).equals(String.valueOf(txtContra2.getPassword()))) {
                pass = String.valueOf(txtContra1.getPassword());
                pass2 = String.valueOf(txtContra2.getPassword());

                try {
                    ClsConnection cn = new ClsConnection();
                    cn.conexion("BasesDeDatos1", "umg", "1234");

                    String query = "INSERT INTO `basesdedatos1`.`usuarios` (`codigo`, `nombre1`,`nombre2`,`apellido1`,`apellido2`,`fechaNac`,`usuario`,"
                            + " `contraseña`, urlFoto) VALUES("
                            + "'" + txtCodigo.getText() + "',"
                            + "'" + txtNombre1.getText() + "',"
                            + "'" + txtNombre2.getText() + "',"
                            + "'" + txtApellido1.getText() + "',"
                            + "'" + txtApellido2.getText() + "',"
                            + "'" + txtNac.getText() + "',"
                            + "'" + txtUsuario.getText() + "',"
                            + "'" + pass + "',"
                            + "'./src/Images/Fotos/" + txtCodigo.getText() + ".jpg'"
                            + ");";

                    cn.insert(query);

                    usuario.nuevoUsuario(txtCodigo.getText(), txtNombre1.getText(), txtNombre2.getText(), txtApellido1.getText(), txtApellido2.getText(),
                            txtNac.getText(), txtUsuario.getText(), pass, pass2,
                            "./src/Images/Fotos/U " + txtCodigo.getText() + ".jpg");

                    //borrarTextos();

                    JOptionPane.showMessageDialog(null, "Usuario creado exitosamente");

                    cn.close();

                } catch (Exception e) {

                }

            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden\nIntente de nuevo", "Adverventcia",
                        JOptionPane.WARNING_MESSAGE);

                this.txtContra1.requestFocus();
            }
        }
    }//GEN-LAST:event_btnCrearActionPerformed

