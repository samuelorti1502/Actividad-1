    public void validarCampos() {
        setMensaje("");
        setValidador(false);

        String[] image = lblImagen.getIcon().toString().split("/");

        if (image[image.length - 1].equals("pinwi.png")) {
            setMensaje(getMensaje() + "La fotografía no puede estar vacia.\n");
        }

        if (txtCodigo.getText().equals("")) {
            setMensaje(getMensaje() + "El campo " + lblCodigo.getText() + " no puede estar vacio.\n");
        }
        if (txtNombre1.getText().equals("")) {
            setMensaje(getMensaje() + "El campo " + lblNombre1.getText() + " no puede estar vacio.\n");
        }
        /*if (txtNombre2.getText().equals("")) {
            mensaje += "El campo " + lblNombre2.getText() + " no puede estar vacio.\n";
        }*/
        if (txtApellido1.getText().equals("")) {
            setMensaje(getMensaje() + "El campo " + lblApellido1.getText() + " no puede estar vacio.\n");
        }
        /*if (txtApellido2.getText().equals("")) {
            mensaje += "El campo " + lblApellido2.getText() + " no puede estar vacio.\n";
        }*/
        if (txtNac.getText().equals("")) {
            setMensaje(getMensaje() + "El campo " + lblNac.getText() + " no puede estar vacio.\n");
        }
        if (txtUsuario.getText().equals("")) {
            setMensaje(getMensaje() + "El campo " + lblUsuario.getText() + " no puede estar vacio.\n");
        }
        if (String.valueOf(txtContra1.getPassword()).equals("")) {
            setMensaje(getMensaje() + "El campo " + lblContra1.getText() + " no puede estar vacio.\n");
        }

        if (!mensaje.equals("")) {
            JOptionPane.showMessageDialog(null, "Error\n\n" + this.getMensaje());
        } else {
            setValidador(true);
        }

    }

